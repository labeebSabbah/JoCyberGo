<script>
  const orderQueue = []; // Array to hold orders

  // Function to add a new order to the first grid and the queue
  function addOrderToGrid(order) {
    const sortableGrid = document.getElementById('sortable-grid');
    const gridItem = createGridItem(order, 'grid-item');
    sortableGrid.appendChild(gridItem);

    // Add the order to the processing queue
    orderQueue.push(order);
  }

  // Function to start processing orders
  function startProcessingOrders() {
    const sortableGrid = document.getElementById('sortable-grid');
    const processedGrid = document.getElementById('processed-grid');

    if (orderQueue.length > 0) {
      const currentOrder = orderQueue.shift(); // Take the first order from the queue

      // Move the processed order from the first grid to the "Processed Orders" grid
      const gridItem = document.getElementById(`order-${currentOrder}`);
      sortableGrid.removeChild(gridItem);
      const processedItem = createGridItem(currentOrder, 'processed-item');
      processedGrid.appendChild(processedItem);
    }
  }

  // Function to create a grid item
  function createGridItem(content, className) {
    const item = document.createElement('div');
    item.id = `order-${content}`;
    item.className = className;
    item.textContent = content;
    return item;
  }

  // Initialize the sortable grid
  document.addEventListener('DOMContentLoaded', function () {
    initializeSortable('items');
  });

  // Function to initialize the sortable grid
  function initializeSortable(gridId) {
    new Sortable(document.getElementById(gridId), {
      animation: 150,
      onEnd: function (evt) {
        // Handle the event when an order is dragged and dropped
        const draggedOrder = evt.from.children[evt.oldIndex].textContent;
        console.log(`Order moved: ${draggedOrder}`);

        // Add the dragged order to the processing queue
        orderQueue.push(draggedOrder);

        // Start processing orders
        startProcessingOrders();
      }
    });
  }

</script>

